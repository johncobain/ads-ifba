#include <stdio.h>

int main(){
    int i, num, bit, base, bin;
    char ch = ' ';
    
    printf("\033[32m|---------------------------------------------------------------------|\n");
    printf("|----------------------------TABELA-ASCII-----------------------------|\n");
    printf("|---------------------------------------------------------------------|\n");
    for(i=32;i<128;i++){
        base = 1; bin = 0;
        for(num = ch; num>0; num/=2){
            bit = num%2;
            bin+= bit*base;
            base*=10;
        }
        printf("|                |             |            |             |           |\n");
        if(ch==127)printf("| Bin: %08d  |  Oct: %04o  |  Dec: %03d  |  Hex: 0X%02x  |  Char:%c    |\n",bin, ch, ch, ch, ch);
        else printf("| Bin: %08d  |  Oct: %04o  |  Dec: %03d  |  Hex: 0X%02x  |  Char:%c   |\n",bin, ch, ch, ch, ch);
        ch++;
    }
    printf("|---------------------------------------------------------------------|\n");
    printf("|--------------------------BY-JOHN-COBAIN-----------------------------|\n");
    printf("|---------------------------------------------------------------------|\n");
 
    return 0;
}